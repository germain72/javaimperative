
public class LangMath {

	public static void main(String[] args) {

		System.out.println("Math.E = " + Math.E);
		System.out.println("Math.PI = " + Math.PI);
		System.out.println("Math.abs(-2) = " + Math.abs(-2));
		System.out.println("Math.ceil(4.5) = " + Math.ceil(4.5));
		System.out.println("Math.floor(4.5) = " + Math.floor(4.5));
		System.out.println("Math.min(3, 10) = " + Math.min(3, 10));
		System.out.println("Math.max(3, 10) = " + Math.max(3, 10));
		System.out.println("Math.random() = " + Math.random());
		System.out.println("Math.pow(3, 2) = " + Math.pow(3, 2));
		System.out.println("Math.round(3.2) = " + Math.round(3.2));
	}

}
